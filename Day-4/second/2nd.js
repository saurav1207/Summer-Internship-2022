function mul(num1, num2){
    num1 = document.getElementById("one").value;
    num2 = document.getElementById("two").value;
    document.getElementById('result').innerHTML=(num1 * num2);
}
function div(num1, num2){
    num1 = document.getElementById("one").value;
    num2 = document.getElementById("two").value;
    document.getElementById('result').innerHTML=(num1 / num2);
}